          <el-input v-model="ruleForm.dianhuahaoma"               placeholder="电话号码" clearable></el-input>
        </el-form-item>
        <el-form-item :style='{"width":"50%","margin":"0 0 20px 0"}'   v-if="flag=='yanjiusheng'"  label="专业" prop="zhuanye">
